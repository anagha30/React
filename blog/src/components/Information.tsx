export function SweetPotato() {
    return (
        <div>
            <h5>INGREDIENTS</h5>
            <ul>
                <li>1 tablespoon oil</li>
                <li>2 shallots, thinly sliced</li>
                <li>2 sweet potatoes, peeled and cubed</li>
                <li>3–4 cups fresh baby spinach</li>
                <li>2–3 tablespoons curry paste (see notes)</li>
                <li>1 14-ounce can regular coconut milk</li>
                <li>1/2 to 1 cup broth or water</li>
                <li>1/2 cup chopped peanuts and cilantro</li>
                <li>fish sauce to taste</li>
            </ul>

            <h5>Instructions</h5>
            <ul>
                <li>Start your rice, if you’re serving this over rice (highly recommend)! Make your HOMEMADE curry paste (you rockstar, you). Roast your garlic, shallots, and ginger. Hydrate your chili peppers. Blitz it all up in the food processor with some spices, lemongrass paste, and cilantro. </li>
                <li>Heat the oil over medium high heat. Add the shallots and stir fry until soft and fragrant. Add the sweet potatoes and stir to coat with oil. Add the curry paste and stir until well-combined.</li>
                <li>Add the coconut milk and broth and let it simmer over low heat for 10-15 minutes until thickened. Stir in the spinach until wilted.</li>
                <li>Add half of the peanut/cilantro mixture; reserve the rest for topping. Add a quick splash of fish sauce to the curry to make the whole thing just sing harmony in your mouth. Yummy.</li>
                <li>Serve over rice, topped with remaining peanuts/cilantro.</li>
            </ul>
        </div>
    )
}

